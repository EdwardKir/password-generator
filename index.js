const genBtn = document.getElementById("button-gen")
const firstInput = document.getElementById("first-input")
const secondInput = document.getElementById("second-input")

let randomCharLength = 15

function makeRandChar() {
  let randChar = Math.floor(Math.random() * characters.length)
  return characters[randChar]
}

function makeRandPas() {
  let randPas = ""
  for (let i = 0; i < randomCharLength; i++) {
    randPas += makeRandChar()
  }
  return randPas
}

genBtn.addEventListener("click", function () {
  firstInput.value = makeRandPas()
  secondInput.value = makeRandPas()
})
